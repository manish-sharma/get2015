package com;

public class CharAtExample {

	 public static void main(String args[]) {
	      String sample = "Strings are immutable";
	      System.out.println("Value of the character at index 8 is : "+sample.charAt(8));
	      int result = sample.charAt(8);
	      System.out.println("Ascii Value of the character at index 8 is : "+result);
	   }
}
