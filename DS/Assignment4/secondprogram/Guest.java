package secondprogram;
/**
 * Class that information about guest user
 * @author Riddhi
 *
 */
public class Guest {

	private int age;        //age of guest
	private String name;   //name of guest

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
