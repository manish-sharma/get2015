/**
 * This class act as class for title_author table containing getter setter method
 * 
 * @author Shishir Date 16th September 2015
 */
public class Title_Author {
	private int title_id;// variable for title_id
	private int author_id;// variable for author_id
   
	// getter for title_id
	public int getTitle_id() {
		return title_id;
	}
	// setter for title_id
	public void setTitle_id(int title_id) {
		this.title_id = title_id;
	}
	// getter for author_id
	public int getAuthor_id() {
		return author_id;
	}
	// setter for author_id
	public void setAuthor_id(int author_id) {
		this.author_id = author_id;
	}

}
