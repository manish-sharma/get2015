/**
 * This class act as class for Publishers table containing getter setter method
 * 
 * @author Shishir Date 16th August 2015
 */
public class Publishers {
	private int publishers_id;// variable for publisher_id
	private String publisher_name;// variable for publisher_name

	// getter for publisher_id
	public int getPublishers_id() {
		return publishers_id;
	}

	// setter for publisher_id
	public void setPublishers_id(int publishers_id) {
		this.publishers_id = publishers_id;
	}

	// getter for publisher_name
	public String getPublisher_name() {
		return publisher_name;
	}

	// setter for publisher_name
	public void setPublisher_name(String publisher_name) {
		this.publisher_name = publisher_name;
	}

}
