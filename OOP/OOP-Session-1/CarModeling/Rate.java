/*
 * This class will maintain the car model correspondse to rate of service
 * @author Banwari
 */
public class Rate {
   String carModel;
   int rate;
   /*
    * Parameterized constructor 
    * @param carModel is the car model name
    * @param rate is the car service rate  
    */
   Rate(String carModel,int rate){
	   this.carModel=carModel;
	   this.rate=rate;
	   
   }
}
