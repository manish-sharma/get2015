package MainClass;

public class Passenger
{
	String passengerName;
	int seatsOrWeightBooked;
	
	public String getPassengerName() 
	{
		return passengerName;
	}
	public void setPassengerName(String passengerName) 
	{
		this.passengerName = passengerName;
	}
	public int getSeatsOrWeightBooked() 
	{
		return seatsOrWeightBooked;
	}
	public void setSeatsOrWeightBooked(int seatsOrWeightBooked) 
	{
		this.seatsOrWeightBooked = seatsOrWeightBooked;
	}
}