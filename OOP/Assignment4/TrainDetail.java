package com.metacube.menu;
/**
 * @author Dheeraj
 * Implement the of Train Reservation System
 * Class TrainDetail it will provide all the getter and setter method to access attributes of train
 */




public class TrainDetail 
{
	public static final String Comparators = null;
	private String trainNumber;
	private String trainName;
	private String typeTrain;
	private String source;
	private String destination;
	private String duration;
	private String seatAvailable;

	public String getTrainNumber() {
		return trainNumber;
	}

	public void setTrainNumber(String trainNumber) {
		this.trainNumber = trainNumber;
	}

	public String getTrainName() {
		return trainName;
	}

	public void setTrainName(String trainName) {
		this.trainName = trainName;
	}

	public String getTypeTrain() {
		return typeTrain;
	}

	public void setTypeTrain(String typeTrain) {
		this.typeTrain = typeTrain;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public String getDestination() {
		return destination;
	}

	public void setDestination(String destination) {
		this.destination = destination;
	}

	public String getDuration() {
		return duration;
	}

	public void setDuration(String duration) {
		this.duration = duration;
	}

	public String getSeatAvailable() {
		return seatAvailable;
	}

	public void setSeatAvailable(String seatAvailable) {
		this.seatAvailable = seatAvailable;
	}

	
	

	 
	      
	

}
